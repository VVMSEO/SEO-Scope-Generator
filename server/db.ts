export async function readDB(): Promise<DatabaseSchema> {
  try {
    const data = await fs.readFile(DB_FILE, 'utf-8');
    const parsed = JSON.parse(data) as DatabaseSchema;
    // Backwards compatibility / ensure fallback structure exists
    if (!parsed.checklists) parsed.checklists = [];
    if (!parsed.tasks) parsed.tasks = [];
    if (!parsed.projects) parsed.projects = [];
    if (!parsed.llm_providers) parsed.llm_providers = [];
    if (!parsed.generated_scopes) parsed.generated_scopes = [];
    return parsed;
  } catch (error) {
    // File doesn't exist or is corrupted, create and seed it
    const initialDB: DatabaseSchema = {
      checklists: DEFAULT_CHECKLISTS,
      tasks: DEFAULT_TASKS,
      projects: DEFAULT_PROJECTS,
      llm_providers: [DEFAULT_LLM_PROVIDER],
      generated_scopes: []
    };
    await writeDB(initialDB);
    return initialDB;
  }
}

export async function writeDB(db: DatabaseSchema): Promise<void> {
  await fs.writeFile(DB_FILE, JSON.stringify(db, null, 2), 'utf-8');
}
