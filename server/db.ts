// Helper to construct a parsed & normalized task
function makeTask(
  id: number,
  checklistId: number,
  section: string,
  rawTask: string,
  priority: string,
  month: number,
  block: string,
  workType: string,
  process: string,
  result: string,
  artifact: string,
  contractText: string,
  clientText: string,
  internalText: string,
  acceptance: string,
  role: string = 'SEO-специалист',
  repeat: string = 'Разово',
  appliesConfig: Partial<Task['applicability']> = {}
): Task {
  return {
    id,
    checklist_id: checklistId,
    section,
    raw_task: rawTask,
    priority,
    default_month: month,
    work_block: block,
    work_type: workType,
    process_text: process,
    result_text: result,
    artifact_type: artifact,
    contract_text: contractText,
    client_text: clientText,
    internal_text: internalText,
    acceptance_criteria: acceptance,
    responsible_role: role,
    repeatability: repeat,
    status: 'normalized',
    created_at: new Date().toISOString(),
    updated_at: new Date().toISOString(),
    applicability: {
      id,
      task_id: id,
      applies_to_all: true,
      applies_to_ecommerce: false,
      applies_to_services: false,
      applies_to_local: false,
      applies_to_blog: false,
      applies_to_ymyl: false,
      requires_catalog: false,
      requires_filters: false,
      requires_multilingual: false,
      requires_migration: false,
      requires_local_business: false,
      exclude_if_no_blog: false,
      exclude_if_no_ecommerce: false,
      exclude_if_no_multilingual: false,
      exclude_if_no_local_seo: false,
      ...appliesConfig
    }
  };
}

